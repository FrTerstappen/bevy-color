function recordKeys<K extends PropertyKey, T>(object: Record<K, T>) {
    return Object.keys(object) as (K)[];
};

export function getCssRegExps(): RegExp[] {
    const names = recordKeys(CSS_COLORS);
    const nameRegexPart = names.join("|");

    const nameSpacePart = `${nameSpaceBaseRegExp}(?:palettes::)?(?:css::)`;

    const regexString = `${nameSpacePart}(${nameRegexPart})`;
    const regex = new RegExp(regexString, "g");

    return [regex];
}

export function extractCssColor(match: RegExpExecArray): Color | undefined {
    const name = match[1];
    const values = CSS_COLORS[name];

    const color = new Color(
        values[0],
        values[1],
        values[2],
        values[3]
    );

    return color;
}
